#include "main.h"
/**
 * print_diagonal - Function to print diagonal line
 * @n: Parameter passed
 * Return: Always 0 on Success
 */
void print_diagonal(int n)
{
	if (n <= 0)
	{
		_putchar('\n');
	}
	else
	{
		int i, j;
			for (i = 0; i < n; i++)
		{
			for (j = 0; j < i; j++)
			{
				_putchar(' ');
			}
			_putchar('\\');
			_putchar('\n');
		}
	}
}
